#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn parse_bus_connection_defaults() {
        let v = serde_json::json!({ "port": "/dev/ttyUSB0" });
        let c: BusConnection = serde_json::from_value(v).unwrap();
        assert_eq!(c.port, "/dev/ttyUSB0");
        assert_eq!(c.baudrate, 19_200);
        assert_eq!(c.master_address, 2);
        assert_eq!(c.slot_bits, 300);
    }

    #[test]
    fn parse_bus_connection_explicit() {
        let v = serde_json::json!({
            "port": "/dev/ttyS0", "baudrate": 93750,
            "master_address": 5, "slot_bits": 400
        });
        let c: BusConnection = serde_json::from_value(v).unwrap();
        assert_eq!(c.baudrate, 93_750);
        assert_eq!(c.master_address, 5);
        assert!(matches!(c.profirust_baudrate(), profirust::Baudrate::B93750));
    }

    #[test]
    fn parse_peripheral_address() {
        let v = serde_json::json!({
            "station_address": 7,
            "ident_number": 1234,
            "max_tsdr": 150,
            "config_bytes": [1, 2, 3],
            "param_bytes": [0xAB, 0xCD],
            "input_bytes": 4,
            "output_bytes": 2
        });
        let a: PeripheralAddress = serde_json::from_value(v).unwrap();
        assert_eq!(a.station_address, 7);
        assert_eq!(a.ident_number, 1234);
        assert_eq!(a.input_bytes, 4);
        assert_eq!(a.output_bytes, 2);
        assert_eq!(a.config_bytes, vec![1, 2, 3]);
    }

    #[test]
    fn parse_peripheral_address_minimal() {
        let v = serde_json::json!({
            "station_address": 3,
            "input_bytes": 8,
            "output_bytes": 0
        });
        let a: PeripheralAddress = serde_json::from_value(v).unwrap();
        assert_eq!(a.station_address, 3);
        assert!(a.config_bytes.is_empty());
        assert!(a.param_bytes.is_empty());
        assert_eq!(a.ident_number, 0);
    }

    #[test]
    fn parse_point_address_byte() {
        let v = serde_json::json!({
            "direction": "input", "byte_offset": 2
        });
        let a: PointAddress = serde_json::from_value(v).unwrap();
        assert!(a.is_input());
        assert_eq!(a.byte_offset, 2);
        assert!(a.bit_offset.is_none());
    }

    #[test]
    fn parse_point_address_bit() {
        let v = serde_json::json!({
            "direction": "input", "byte_offset": 0,
            "bit_offset": 3, "bit_count": 1
        });
        let a: PointAddress = serde_json::from_value(v).unwrap();
        assert_eq!(a.bit_offset, Some(3));
        assert_eq!(a.bit_count, Some(1));
    }

    #[test]
    fn parse_point_address_output() {
        let v = serde_json::json!({ "direction": "output", "byte_offset": 0 });
        let a: PointAddress = serde_json::from_value(v).unwrap();
        assert!(!a.is_input());
    }

    #[test]
    fn baudrate_mapping_exhaustive() {
        for (baud, variant) in [
            (9_600u32, profirust::Baudrate::B9600),
            (19_200, profirust::Baudrate::B19200),
            (93_750, profirust::Baudrate::B93750),
            (187_500, profirust::Baudrate::B187500),
            (500_000, profirust::Baudrate::B500000),
            (1_500_000, profirust::Baudrate::B1500000),
            (3_000_000, profirust::Baudrate::B3000000),
            (6_000_000, profirust::Baudrate::B6000000),
            (12_000_000, profirust::Baudrate::B12000000),
        ] {
            let c = BusConnection {
                baudrate: baud,
                ..Default::default()
            };
            // Just verify it maps to the right variant (PartialEq on Baudrate)
            assert_eq!(
                std::mem::discriminant(&c.profirust_baudrate()),
                std::mem::discriminant(&variant),
                "baudrate {baud} mapped to wrong variant"
            );
        }
    }
}
